"""
Shadow prefilter evaluation — full corpus baseline analysis.

Measures recall, false negative rate, and cost impact at multiple thresholds
against the full scored-evidence baseline.  Produces a structured result that
can be rendered as Markdown.

Usage:
    from poea.assignments.prefilter_eval import PrefilterEvaluator
    evaluator = PrefilterEvaluator(evidence_units, concepts, scored_records)
    result = evaluator.evaluate(thresholds=[0.0, 0.01, 0.03, 0.05, 0.10])
    md = result.to_markdown()
"""
from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any

from ..evidence.schemas import EvidenceUnit
from ..registry.schemas import ConceptEntry
from .prefilter import ShadowPrefilter, _concept_keywords

_TOKENS_PER_RECORD = 1250
_COST_PER_M_INPUT_TOKENS = 1.74


@dataclass(frozen=True)
class FalseNegative:
    evidence_id: str
    evidence_title: str
    concept_name: str
    verdict: str
    relevance: float
    is_sparse: bool
    matched_keywords: list[str]
    total_keywords: int
    why_missed: str

    def to_row(self) -> str:
        v = "TRUE" if self.verdict == "TRUE" else "FALSE"
        return (
            f"| `{self.evidence_id[:14]}` | {_esc(self.evidence_title[:45])} "
            f"| {_esc(self.concept_name)} | {v} | {self.relevance:.4f} "
            f"| {_esc(self.why_missed[:60])} |"
        )


@dataclass
class ThresholdResult:
    threshold: float
    total_pairs: int
    skipped_pairs: int
    skipped_records: int
    false_negatives: int
    fn_true: int
    fn_false: int
    total_observed: int
    fn_list: list[FalseNegative] = field(default_factory=list)

    @property
    def skip_rate(self) -> float:
        return self.skipped_pairs / self.total_pairs if self.total_pairs else 0.0

    @property
    def recall(self) -> float:
        return (self.total_observed - self.false_negatives) / self.total_observed if self.total_observed else 1.0

    @property
    def fn_rate(self) -> float:
        return self.false_negatives / self.total_observed if self.total_observed else 0.0

    @property
    def estimated_cost_savings_usd(self) -> float:
        return self.skipped_records * _TOKENS_PER_RECORD / 1_000_000 * _COST_PER_M_INPUT_TOKENS

    def to_table_row(self) -> str:
        return (
            f"| {self.threshold:.2f} | {self.skip_rate:.1%} | {self.skipped_pairs} "
            f"| {self.skipped_records} | {self.false_negatives} | {self.recall:.1%} "
            f"| ~${self.estimated_cost_savings_usd:.4f} |"
        )


@dataclass
class EvaluationResult:
    total_pairs: int
    total_records: int
    total_concepts: int
    total_observed: int
    total_true: int
    total_false: int
    total_neutral: int
    threshold_results: list[ThresholdResult]
    recommendation: str
    recommendation_reason: str

    def to_markdown(self) -> str:
        lines: list[str] = []
        lines.append("# Shadow Prefilter Evaluation")
        lines.append("")
        lines.append("_Generated by `poea.assignments.prefilter_eval`_")
        lines.append("")
        _append_executive_summary(lines, self)
        _append_threshold_table(lines, self)
        _append_false_negative_analysis(lines, self)
        _append_cost_savings_analysis(lines, self)
        _append_recommendation(lines, self)
        return "\n".join(lines)


class PrefilterEvaluator:
    """
    Evaluates the shadow prefilter against a fully-scored evidence baseline.

    Takes the actual LLM-scored assignments as ground truth and measures
    recall, false-negative rate, and cost impact at multiple thresholds.
    Does not change any scoring behavior — measurement only.
    """

    def __init__(
        self,
        evidence_units: list[EvidenceUnit],
        concepts: list[ConceptEntry],
        scored_records: list[dict[str, Any]],
        tokens_per_record: int = _TOKENS_PER_RECORD,
        cost_per_m_tokens: float = _COST_PER_M_INPUT_TOKENS,
    ) -> None:
        self._evidence = evidence_units
        self._concepts = concepts
        self._tokens_per_record = tokens_per_record
        self._cost_per_m = cost_per_m_tokens

        self._actual: dict[tuple[str, str], bool | None] = {}
        for rec in scored_records:
            ev_id = rec.get("evidence_id", "")
            for a in rec.get("assignments", []):
                self._actual[(ev_id, a.get("variable_name", ""))] = a.get("assigned_value")

        self._observed: set[tuple[str, str]] = {
            (ev_id, cname)
            for (ev_id, cname), v in self._actual.items()
            if v is True or v is False
        }
        self._ev_by_id = {u.evidence_id: u for u in evidence_units}
        self._c_by_name = {c.name: c for c in concepts}
        self._prefilter = ShadowPrefilter(tokens_per_pair=15, cost_per_m_tokens=cost_per_m_tokens)

        self._relevance: dict[tuple[str, str], float] = {}
        for ev in evidence_units:
            for c in concepts:
                self._relevance[(ev.evidence_id, c.name)] = self._prefilter.relevance(ev, c)

    def evaluate(
        self,
        thresholds: list[float] | None = None,
    ) -> EvaluationResult:
        if thresholds is None:
            thresholds = [0.00, 0.01, 0.03, 0.05, 0.10]

        n_obs = len(self._observed)
        n_true = sum(1 for k in self._observed if self._actual[k] is True)
        n_false = sum(1 for k in self._observed if self._actual[k] is False)
        n_total = len(self._evidence) * len(self._concepts)
        n_neutral = n_total - n_obs

        threshold_results = [self._evaluate_threshold(t) for t in sorted(set(thresholds))]
        recommendation, reason = _make_recommendation(threshold_results)

        return EvaluationResult(
            total_pairs=n_total,
            total_records=len(self._evidence),
            total_concepts=len(self._concepts),
            total_observed=n_obs,
            total_true=n_true,
            total_false=n_false,
            total_neutral=n_neutral,
            threshold_results=threshold_results,
            recommendation=recommendation,
            recommendation_reason=reason,
        )

    def _evaluate_threshold(self, threshold: float) -> ThresholdResult:
        fn_list: list[FalseNegative] = []
        skipped_pairs = 0
        skipped_records = 0
        fn_true = fn_false = 0

        for ev in self._evidence:
            all_skip = True
            for c in self._concepts:
                rel = self._relevance[(ev.evidence_id, c.name)]
                if rel < threshold:
                    skipped_pairs += 1
                    v = self._actual.get((ev.evidence_id, c.name))
                    if v is True or v is False:
                        if v is True:
                            fn_true += 1
                        else:
                            fn_false += 1
                        fn_list.append(_make_fn(ev, c, rel, v))
                else:
                    all_skip = False
            if all_skip:
                skipped_records += 1

        fn_list.sort(key=lambda f: (f.relevance, f.concept_name))

        return ThresholdResult(
            threshold=threshold,
            total_pairs=len(self._evidence) * len(self._concepts),
            skipped_pairs=skipped_pairs,
            skipped_records=skipped_records,
            false_negatives=len(fn_list),
            fn_true=fn_true,
            fn_false=fn_false,
            total_observed=len(self._observed),
            fn_list=fn_list,
        )


