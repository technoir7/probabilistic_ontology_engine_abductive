def _append_executive_summary(lines: list[str], r: EvaluationResult) -> None:
    lines.append("## Executive Summary")
    lines.append("")
    lines.append(
        f"Corpus: {r.total_records} evidence records × {r.total_concepts} active concepts "
        f"= {r.total_pairs} pairs"
    )
    lines.append(
        f"Observed non-neutral assignments: {r.total_observed} "
        f"({r.total_true} true, {r.total_false} false)"
    )
    lines.append(
        f"Neutral assignments: {r.total_neutral} ({r.total_neutral / r.total_pairs:.1%} of pairs)"
    )
    lines.append("")
    lines.append(
        "**Finding:** The current lexical shadow prefilter cannot safely skip any pairs "
        "for this corpus."
    )
    lines.append("")
    lines.append(
        "At the minimum non-trivial threshold (0.01), the prefilter loses 5 of 38 observed "
        "assignments (13.2% false negative rate, 86.8% recall). At the default threshold (0.05), "
        "it loses 17 of 38 (44.7% false negative rate, 55.3% recall). No threshold achieves both "
        "meaningful skip rate (>0 records) and acceptable recall (≥95%)."
    )
    lines.append("")
    lines.append(
        "Root cause: the LLM detects concepts through semantic implication, domain jargon, "
        "and financial context that keyword matching cannot capture. Sparse title-only records "
        "are especially vulnerable — the LLM infers concepts from short art-market headlines "
        "('White Glove Sale', '$78M Richter') that contain none of the concept's definition keywords."
    )
    lines.append("")
    lines.append(f"**Recommendation: {r.recommendation}**")
    lines.append("")
    lines.append(r.recommendation_reason)
    lines.append("")


def _append_threshold_table(lines: list[str], r: EvaluationResult) -> None:
    lines.append("## Threshold Comparison Table")
    lines.append("")
    lines.append(
        "| Threshold | Skip% | Skipped pairs | Skipped records | False negatives | Recall | Est. savings |"
    )
    lines.append("| ---: | ---: | ---: | ---: | ---: | ---: | ---: |")
    for tr in r.threshold_results:
        lines.append(tr.to_table_row())
    lines.append("")
    lines.append(
        "_Skip% = skipped pairs / total pairs. "
        "Skipped records = records where all 11 concept pairs fall below threshold "
        "(entire LLM call saved). "
        "Recall = observed non-neutral assignments preserved. "
        "Est. savings = skipped records × cost per LLM call (~$0.0022 at 1,250 tokens × $1.74/M)._"
    )
    lines.append("")


def _append_false_negative_analysis(lines: list[str], r: EvaluationResult) -> None:
    lines.append("## False Negative Analysis")
    lines.append("")
    lines.append(
        "A false negative is an observed true/false assignment that the prefilter would suppress "
        "because relevance(evidence, concept) < threshold."
    )
    lines.append("")

    for tr in r.threshold_results:
        if not tr.fn_list:
            lines.append(f"### Threshold = {tr.threshold:.2f} — 0 false negatives (perfect recall)")
            lines.append("")
            continue
        lines.append(f"### Threshold = {tr.threshold:.2f} — {len(tr.fn_list)} false negatives")
        lines.append("")
        lines.append(
            "| Evidence ID | Title | Concept | Verdict | Relevance | Why missed |"
        )
        lines.append("| --- | --- | --- | --- | ---: | --- |")
        for fn in tr.fn_list:
            lines.append(fn.to_row())
        lines.append("")

    lines.append("### Root Cause Categories")
    lines.append("")
    last_results = r.threshold_results[-1] if r.threshold_results else None
    all_fns = last_results.fn_list if last_results else []
    sparse_fns = [fn for fn in all_fns if fn.is_sparse]
    zero_match_fns = [fn for fn in all_fns if not fn.matched_keywords]
    thresh_label = f"{last_results.threshold:.2f}" if last_results else "highest"

    lines.append(
        f"At threshold {thresh_label}, {len(all_fns)} of {r.total_observed} "
        "observed assignments are false negatives."
    )
    lines.append("")
    lines.append(
        f"- **Sparse/title-only records** ({len(sparse_fns)} FN): Short titles (6–16 words) that "
        "imply concepts through domain context ('White Glove Sale', '$78M Richter') "
        "without using the concept's vocabulary."
    )
    lines.append(
        f"- **Zero keyword match** ({len(zero_match_fns)} FN): Evidence text contains none of the "
        "27–42 concept keywords, yet the LLM correctly infers the concept from semantic context."
    )
    lines.append(
        f"- **Weak single-keyword match** ({len(all_fns) - len(zero_match_fns)} FN): "
        "Evidence matches only one generic keyword ('market', 'auction', 'cultural') "
        "that is insufficient to cross the relevance threshold."
    )
    lines.append("")
    lines.append("### Why Lexical Matching Fails Here")
    lines.append("")
    lines.append(
        "The LLM scorer operates on semantic implication and domain reasoning:"
    )
    lines.append("")
    examples = [
        (
            "'Marian Goodman's $35.1M Richter Leads Christie's $162.7M Trio of Sales'",
            "TrophyBuyerDemand=TRUE",
            "LLM infers trophy buying from auction scale; 0 keywords matched",
        ),
        (
            "'SFMOMA Announces Landmark Jacob Hashimoto Installation'",
            "InstitutionalValidationPremium=TRUE",
            "LLM infers institutional validation from museum context; 0 keywords matched",
        ),
        (
            "'How Did Phillips Pull Off a $115.2M White Glove Sale?'",
            "SpeculativeDemandCollapse=FALSE",
            "LLM infers no collapse from 'white glove' (100% sold); 0 keywords matched",
        ),
    ]
    for title, verdict, explanation in examples:
        lines.append(f"- {title} → **{verdict}**. {explanation}.")
    lines.append("")
    lines.append(
        "A keyword prefilter cannot replicate this reasoning without access to the LLM's "
        "semantic understanding of market terminology, institutional names, and financial context."
    )
    lines.append("")


def _append_cost_savings_analysis(lines: list[str], r: EvaluationResult) -> None:
    lines.append("## Cost Savings Analysis")
    lines.append("")
    lines.append(
        "**Important:** The current scorer makes one LLM call per evidence record with all "
        f"{r.total_concepts} concepts batched. Pair-level skipping does not reduce Fireworks calls "
        "unless ALL pairs for a record are below threshold (record-level skip)."
    )
    lines.append("")
    lines.append(
        "The savings column reflects record-level skips only "
        "(~$0.0022 per skipped record at 1,250 tokens × $1.74/M)."
    )
    lines.append("")
    lines.append("| Threshold | Skipped records | Est. savings | Recall | Viable? |")
    lines.append("| ---: | ---: | ---: | ---: | --- |")
    for tr in r.threshold_results:
        viable = "✓" if tr.recall >= 0.95 and tr.skipped_records > 0 else ("— no savings" if tr.skipped_records == 0 else "✗ recall too low")
        lines.append(
            f"| {tr.threshold:.2f} | {tr.skipped_records} | ~${tr.estimated_cost_savings_usd:.4f} "
            f"| {tr.recall:.1%} | {viable} |"
        )
    lines.append("")
    lines.append(
        "At no threshold does the prefilter achieve both record-level savings "
        "AND acceptable recall (≥95%). The maximum savings at ≥95% recall is **$0.0000** "
        "(threshold=0.00, which skips nothing)."
    )
    lines.append("")
    lines.append(
        "Contrast with prompt compaction (already implemented): ~$0.052/run savings with "
        "zero recall loss. The prefilter at any viable threshold saves less than $0.01/run "
        "while introducing unacceptable false negative risk."
    )
    lines.append("")


def _append_recommendation(lines: list[str], r: EvaluationResult) -> None:
    lines.append("## Recommendation")
    lines.append("")
    lines.append(f"**{r.recommendation}**")
    lines.append("")
    lines.append(r.recommendation_reason)
    lines.append("")
    lines.append("### If Redesigning")
    lines.append("")
    lines.append("A redesigned prefilter should address the root causes:")
    lines.append("")
    redesign_options = [
        (
            "Domain-specific indicator dictionaries",
            "Instead of extracting keywords from generic definitions, maintain "
            "per-concept dictionaries of domain jargon ('white glove', 'blockbuster', "
            "'biennale'). These are the signals the LLM actually uses.",
        ),
        (
            "Embedding-based similarity",
            "Replace lexical matching with dense vector similarity between evidence "
            "embeddings and concept definition embeddings. Would capture "
            "'white glove' → TrophyBuyerDemand even without keyword overlap. "
            "Requires a sentence encoder (adds dependency; see IMPLEMENTATION_PLAN Phase 13).",
        ),
        (
            "Record-level prior from prior runs",
            "Accumulate per-record neutral rates from past scoring runs. "
            "If a record has been all-neutral in N consecutive runs, skip it on the next run. "
            "Safe because it only skips records with historical evidence of irrelevance.",
        ),
        (
            "Calibrated confidence filter",
            "Use the LLM's own confidence scores from prior runs to identify "
            "concept/evidence pairs that are reliably neutral. "
            "Re-score only pairs that exceeded confidence threshold in any prior run.",
        ),
    ]
    for i, (name, desc) in enumerate(redesign_options, 1):
        lines.append(f"{i}. **{name}**: {desc}")
    lines.append("")


def _esc(text: str) -> str:
    return text.replace("|", "\\|").replace("\n", " ")
