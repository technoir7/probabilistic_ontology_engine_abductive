# Pure helpers
# ---------------------------------------------------------------------------

def _make_fn(ev: EvidenceUnit, c: ConceptEntry, rel: float, v: bool) -> FalseNegative:
    kws = _concept_keywords(c.name, c.definition)
    text_lower = (ev.title + " " + ev.text).lower()
    matched = [kw for kw in kws if kw in text_lower]
    sparse = ev.metadata.get("sparse_text", False)
    why = _explain_miss(rel, matched, sparse)
    return FalseNegative(
        evidence_id=ev.evidence_id,
        evidence_title=ev.title,
        concept_name=c.name,
        verdict="TRUE" if v is True else "FALSE",
        relevance=rel,
        is_sparse=sparse,
        matched_keywords=matched,
        total_keywords=len(kws),
        why_missed=why,
    )


def _explain_miss(rel: float, matched: list[str], sparse: bool) -> str:
    if rel == 0.0:
        if sparse:
            return "Sparse title-only record; no concept keywords in short title"
        return "No concept keywords found in full evidence text"
    if len(matched) == 1:
        return f"Only generic keyword '{matched[0]}' matched; insufficient signal"
    return f"{len(matched)} weak matches; relevance below threshold"


def _make_recommendation(results: list[ThresholdResult]) -> tuple[str, str]:
    min_viable_recall = 0.95
    best_viable = next(
        (r for r in reversed(results) if r.recall >= min_viable_recall and r.skipped_records > 0),
        None,
    )
    if best_viable is None:
        return (
            "C. Redesign prefilter",
            (
                "No threshold achieves both meaningful record-level savings (>0 records skipped) "
                "and acceptable recall (≥95%). The current lexical keyword prefilter is "
                "fundamentally mismatched to the semantic reasoning the LLM uses for this corpus. "
                "Skipping must NOT be enabled. See redesign options in the recommendation section."
            ),
        )
    return (
        f"A. Enable skipping at threshold {best_viable.threshold:.2f}",
        (
            f"Threshold {best_viable.threshold:.2f} achieves {best_viable.recall:.1%} recall "
            f"while saving {best_viable.skipped_records} Fireworks calls "
            f"(~${best_viable.estimated_cost_savings_usd:.4f} per run)."
        ),
    )


